		static GLenum ShaderTypeFromString(const std::string& type)
		{
			if (type == "vertex")
				return GL_VERTEX_SHADER;
			else if (type == "fragment" || type == "pixel" || type == "frag")
				return GL_FRAGMENT_SHADER;

			BG_CORE_ASSERT(false, "Unknown shader type: {0}", type);
			return GL_NONE;
		}

